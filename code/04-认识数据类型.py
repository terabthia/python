'''
1. 按照经验将不同的变量储存为不同类型的数据
2. 验证这些数据是何种类型
'''

# int--整型
a = 1
print(type(a))

# float--浮点型
b = 1.1
print(type(b))

# str--字符型
c = 'trash'
print(type(c))

# bool--布尔型(逻辑判断--True&False)
d = False
print(type(d))

# list--列表
e = [10,20,30]
print(type(e))

# tuple--元组
f = (10,20,30)
print(type(f))

# set--集合
g = {10,20,30}
print(type(g))

# dict--字典
h = {'name':'sheldon','age':19}
print(type(h))
