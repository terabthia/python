a = 10
a += 1  # a = a + 1
print(a)

a = 10
a = a + 1
print(a)

b = 9
b -= 1  # b = b - 1
print(b)

# 相似的，还有*=, /=, //=, %=, **= 等等
c=8
c /= 4
print(c)
c **= 5
print(c)

# 扩展
d = 10
d += 1 + 2
# 是先算1+2=3，然后是d += 3? 还是先算d += 1 再加2？
print(d)

e = 10
e *= 2 + 3
print(e)
# 注意：从加法没法看出来，但从乘法赋值可以看出来是先算的等号右边的表达式
